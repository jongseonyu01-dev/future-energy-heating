export function getSigunguList(sido: string): string[] {
  const found = ADDRESS_DATA.find((s) => s.name === sido);
  return found ? found.sigungus.map((sg) => sg.name) : [];
}

export function getDongList(sido: string, sigungu: string): string[] {
  const foundSido = ADDRESS_DATA.find((s) => s.name === sido);
  if (!foundSido) return [];
  const foundSigungu = foundSido.sigungus.find((sg) => sg.name === sigungu);
  return foundSigungu ? foundSigungu.dongs : [];
}

export function formatFullAddress(params: {
  sido?: string | null;
  sigungu?: string | null;
  eupmyeondong?: string | null;
  buildingName?: string | null;
  apartmentName?: string | null;
  dong?: string | null;
  ho?: string | null;
}): string {
  const { sido, sigungu, eupmyeondong, dong, ho } = params;
  const buildingName = params.buildingName || params.apartmentName;
  const parts: string[] = [];
  if (sido) parts.push(sido);
  if (sigungu) parts.push(sigungu);
  if (eupmyeondong) parts.push(eupmyeondong);
  if (buildingName?.trim()) parts.push(buildingName.trim());
  if (dong?.trim()) parts.push(dong.trim() + "동");
  if (ho?.trim()) parts.push(ho.trim() + "호");
  return parts.filter(Boolean).join(" ");
}

